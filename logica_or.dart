void main() {
  var a = 1;
  var b = 2;

  if ((a > 0) || (b < 3)) {
    print('VERDADEIRO');
  } else {
    print('FALSO');
  }

  if ((a > 1) || (b < 3)) {
    print('VERDADEIRO');
  } else {
    print('FALSO');
  }

  if ((a >= 1) || (b < 3)) {
    print('VERDADEIRO');
  } else {
    print('FALSO');
  }

  if ((a != 1) || (b != 2)) {
    print('VERDADEIRO');
  } else {
    print('FALSO');
  }
}
